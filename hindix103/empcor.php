    <p><br />
      <strong>ACTIVITIES </strong></p>
    <p align="justify">Recognizing the need to regulate  production, promote overseas marketing and control recurring instances of  imbalances in supply and demand, which lead to market problems, the Government  of India under the Tobacco Board Act of 1975, established the Tobacco Board, in  place of the Tobacco Export Promotion Council. The Board came into existence  from 1-1-1976 and opened its head quarters at Guntur in Andhra Pradesh, India.</p>
    <p align="justify">      The Tobacco Board Act aims at  planned development of Tobacco Industry in the country. The various activities  of the Board outlined in the Act for the promotion of the industry are- </p>
    <ul>
      <li>        Regulating the production and curing  of Virginia Tobacco with regard to the demand in India and abroad. </li>
      <li>        Constant monitoring of the Virginia  tobacco market, both in India and abroad and ensuring fair and remunerative  price to the growers and reducing wide fluctuations in the prices of the  commodity. </li>
      <li>        Sustaining and improving the  existing international markets and developing new markets overseas for Indian  Virginia Tobacco and its products and devising marketing strategies in  consonance with demand for the commodity including group marketing under  limited brand names. </li>
      <li>        Establishing auction platforms for  sale of Virginia tobacco by registered growers and functioning as an auctioneer  at auction platforms either established by it or registered with it. </li>
      <li>        Recommending to the Central  Government the minimum prices to be fixed for exportable Virginia tobacco with  a view to avoiding unhealthy competition amongst the exporters. (Under its Exim  policy, the Government decided to abolish fixation of M.E.P. with effect from  1-4-1993). </li>
      <li>        Regulating other aspects of Virginia  tobacco marketing in India and export of Virginia tobacco having due regard to  the interests of growers, manufacturers, dealers and the nation. </li>
      <li>        Propagating information useful to  the growers, dealers and exporters (including packers) of Virginia tobacco and  manufacturers of tobacco products and others concerned. </li>
      <li>        Purchasing Virginia tobacco from  growers when the same is considered necessary or expedient for protecting the  interests of the growers and disposing it in India or abroad as and when  considered appropriate. <br />
        Promoting tobacco grading at the  level of growers. </li>
      <li>Sponsoring, assisting, co-coordinating or  encouraging scientific, technological and economic research for promotion of  tobacco industry. </li>
    </ul>
